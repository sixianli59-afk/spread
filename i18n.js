function getLanguage() {
  return localStorage.getItem("spread_lang") || "zh";
}

function setLanguage(lang) {
  localStorage.setItem("spread_lang", lang);
}

function t(key) {
  const lang = getLanguage();
  return translations[lang]?.[key] || translations.zh[key] || key;
}

function applyTranslations() {
  document.documentElement.lang = getLanguage() === "en" ? "en" : "zh-CN";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = t(key);
    if (typeof value === "string") {
      el.innerHTML = value.replace(/\n/g, "<br />");
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    el.placeholder = t(key);
  });

  document.querySelectorAll("[data-i18n-value]").forEach((el) => {
    const key = el.dataset.i18nValue;
    el.textContent = t(key);
  });

  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.langBtn === getLanguage());
  });

  const titleMap = {
    "/index.html": "$pread",
    "/generate.html": getLanguage() === "en" ? "$pread - Copy Generator" : "$pread - 商品文案生成",
    "/analyze.html": getLanguage() === "en" ? "$pread - Market Fit Analysis" : "$pread - 市场适配分析"
  };

  const path = location.pathname.endsWith("/") ? "/index.html" : location.pathname;
  document.title = titleMap[path] || "$pread";
}

function bindLanguageSwitcher() {
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    btn.addEventListener("click", () => {
      setLanguage(btn.dataset.langBtn);
      applyTranslations();
      if (typeof window.refreshDynamicLanguage === "function") {
        window.refreshDynamicLanguage();
      }
    });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  bindLanguageSwitcher();
  applyTranslations();
});
